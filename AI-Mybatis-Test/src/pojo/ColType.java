package pojo;

public class ColType {
	private int itemId;
	private String frameid;
	private int total_rows;
	private String desttypes;
	private String rowlist;
	private int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public String getFrameid() {
		return frameid;
	}

	public void setFrameid(String frameid) {
		this.frameid = frameid;
	}

	public int getTotal_rows() {
		return total_rows;
	}

	public void setTotal_rows(int total_rows) {
		this.total_rows = total_rows;
	}

	public String getDesttypes() {
		return desttypes;
	}

	public void setDesttypes(String desttypes) {
		this.desttypes = desttypes;
	}

	public String getRowlist() {
		return rowlist;
	}

	public void setRowlist(String rowlist) {
		this.rowlist = rowlist;
	}
}
